/*
   - typeof: mostra o tipo da variável;
   - var: podem ser atualizadas e declaradas novamente;
   - const: não podem ser atualizadas nem declaradas novamente;
   - let: podem ser atualizadas, mas não podem ser declaradas novamente;
   - ${} substitui a concatenação;
*/

// Métodos //

/*
    - toUpperCase(): maisúsculo na saída;
    - toLowerCase(): minúsculo na saída;
    - split(): corta cada String até a próxima vírgula (",") {sem vírgula, ele corta cada caractere};
    - toString(): converte em String;
    - push(): adiciona um elemento no final do Array;
    - unshift(): adiciona um elemento no início do Array;
    - shift(): remove
    - pop(): remove o último elemento do Array;
    - list[posição] = "String ou número": renomeia a variável;
    - indexOf(): mostra a posição do elemento no Array;
    - sort(): ordena os elementos em ordem alfabética;
    - length(): mostra quantos elementos a lista possui;
    - variável = Array.isArray(nome da lista): verifica se a variável é um Array;

    * Recebem função:*
    - map(): executa uma função em todos os elementos do Array
        Exemplo: lista.map()function(variável){
            return variável + (ação)    
        });
    - filter(): filtra o Array de acordo com a função estabelecida e adiciona no novo Array;
    - reduce(): reduz o Array para um elemento;
*/

